								Outras abordagens também se mostraram eficazes. A Coreia do Sul teve ótimos resultados, principalmente no início, com o uso de testagem em massa. Numa estratégia mais barata, o Vietnã rastreou e isolou os casos de contágio.<br/>
								Até a última semana, registrava apenas 35 óbitos. Na Nova Zelândia, a primeira-ministra Jacinta Ardern conseguiu cativar a população em um esforço de comunicação que fez o país atravessar coletivamente o lockdown mais severo da sua história.<br/>
