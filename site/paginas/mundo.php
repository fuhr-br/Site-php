							<p>
								Países que assumiram uma postura mais rígida em relação à pandemia da Covid-19 mostram que, mais do que apostar em estratégias inovadoras, é necessário uma liderança central forte para vencer a crise sanitária.<br/>
								Um exemplo disso são os Estados Unidos, que, enquanto estava sob a liderança de Donald Trump, que minimizava a gravidade da doença, o país esteve imerso no caos, liderando o número de óbitos e novos casos no mundo.<br/>
								Agora, sob a liderança de Joe Biden, o país vive um novo momento, aposta na vacinação (são 3,2 milhões doses diárias) e nas demais medidas para conter a pandemia. Com isso, desde o último dia 19, todos acima de 16 anos já conseguem se vacinar.<br/>
